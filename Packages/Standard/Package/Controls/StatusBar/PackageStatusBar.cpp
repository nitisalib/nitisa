		IControl *CPackageStatusBar::Create(IPropertyList **properties, IEventList **events)
		{
			IControl *result{ new CStatusBar() };
			if (properties)
				*properties = new CStatusBarPropertyList(getPackage(), result, this);
			if (events)
				*events = new CStatusBarEventList(getPackage(), result, this);
			if (properties || events)
				getPackage()->QueryService()->NotifyOnCreateControl(result, this, *properties, *events);
			return result;
		}

