		bool CPackageStatusBar::CreateListItem(IComponent *component, IListItem *parent, IListItem **item, IPackageControlListener *listener)
		{
			if (component && (item || listener))
			{
				IStatusBar *control{ cast<IStatusBar*>(component) };
				if (control)
				{
					IListItem *i{ new CListItemStatusPanelLabel(L"Panel " + ToString(control->getItemCount())) };
					if (item)
						*item = i;
					control->AddItem(i);
					if (listener)
						listener->OnCreateListItem(this, parent, i);
					return true;
				}
			}
			return false;
		}

		void CPackageStatusBar::Save(Variant &dest, IPropertyList *properties, IEventList *events)
		{
			CPackageControl::Save(dest, properties, events);
			CStatusBar *control{ cast<CStatusBar*>(properties->getParent()) };
			if (Application->Editor && control)
				for (int i = 0; i < control->getItemCount(); i++)
				{
					IListItem *item{ control->getItem(i) };
					IPackageListItem *package_item{ Application->Editor->getListItem(item) };
					Variant data;
					if (package_item)
					{
						data[L"Package"] = package_item->getPackage()->getName();
						data[L"ClassName"] = package_item->getClassName();
						IPropertyList *properties;
						IListItem *test{ package_item->Create(&properties) };
						properties->setParent(item, this);
						test->Release();
						properties->Save(data);
						properties->Release();
					}
					dest[L"Items"].push_back(data);
				}
		}

