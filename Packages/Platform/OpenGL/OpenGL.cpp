	COpenGL *COpenGL::Create(IErrorListener *listener)
	{
		Version2 ver;
		if (!ngl::loaded(&ver))
			return nullptr;
		dglListener(listener);
		COpenGL *result{ new COpenGL(listener ? true : false) };
		result->m_sVersion = ver;
		return result;
	}

#pragma region COpenGLShader
	COpenGLShader::COpenGLShader(COpenGL *graphics, const AnsiString &shader_text, const GLenum type) :
		m_pGraphics{ graphics },
		m_hID{ 0 },
		m_bCompiled{ false }
	{
		if ((m_hID = m_pGraphics->CreateShader(type)) > 0)
		{
			const char *p[1]{ shader_text.c_str() };
			int l[1]{ (int)shader_text.length() }, success;
			m_pGraphics->ShaderSource(m_hID, 1, p, l);
			m_pGraphics->CompileShader(m_hID);
			m_pGraphics->GetShaderiv(m_hID, GL_COMPILE_STATUS, &success);
			
			GLsizei length;
			m_pGraphics->GetShaderiv(m_hID, GL_INFO_LOG_LENGTH, &length);
			if (length > 0)
			{
				GLchar *log{ new GLchar[length] };
				m_pGraphics->GetShaderInfoLog(m_hID, length, nullptr, log);
				m_sError = log;
				delete[] log;
			#ifdef _DEBUG
				if (m_sError.length() > 0)
					std::cerr << m_sError << std::endl;
			#endif
			}

			if (!success)
			{
				m_pGraphics->DeleteShader(m_hID);
				m_hID = 0;
			}
			else
				m_bCompiled = true;
		}
	}
#pragma endregion

#pragma region COpenGLProgram
	bool COpenGLProgram::Link()
	{
		int success;
		m_pGraphics->LinkProgram(m_hID);
		m_pGraphics->GetProgramiv(m_hID, GL_LINK_STATUS, &success);
		if (success)
			m_bLinked = true;
		GLsizei length;
		m_pGraphics->GetProgramiv(m_hID, GL_INFO_LOG_LENGTH, &length);
		if (length > 0)
		{
			GLchar *log{ new GLchar[length] };
			m_pGraphics->GetProgramInfoLog(m_hID, length, nullptr, log);
			m_sError = log;
			delete[] log;
		#ifdef _DEBUG
			if (m_sError.length() > 0)
				std::cerr << m_sError << std::endl;
		#endif
		}
		return m_bLinked;
	}
#pragma endregion
