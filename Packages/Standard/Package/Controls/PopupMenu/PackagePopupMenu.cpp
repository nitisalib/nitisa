		IControl *CPackagePopupMenu::Create(IPropertyList **properties, IEventList **events)
		{
			IControl *result{ new CPopupMenu() };
			if (properties)
				*properties = new CPopupMenuPropertyList(getPackage(), result, this);
			if (events)
				*events = new CPopupMenuEventList(getPackage(), result, this);
			if (properties || events)
				getPackage()->QueryService()->NotifyOnCreateControl(result, this, *properties, *events);
			return result;
		}

